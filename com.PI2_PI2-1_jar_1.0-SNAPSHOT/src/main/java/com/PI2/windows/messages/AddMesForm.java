    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        try {
            validateRequiredFields();
            DatabaseConnection databaseConnection = new DatabaseConnection(
            "root", Env.getPass(), "manager_messages", "localhost", 3306);
            MessageDao messageDao = new MessageDao(databaseConnection); 
            
            Message message = new Message(messageArea.getText());
            if (this.getTitle().startsWith("Ed")) { //update               
                message.setId(Home.getMsgIdForUpdate(sendboxTable));
                messageDao.update(message);
            } else if (this.getTitle().startsWith("R")){ //save 
                JTable jt = Home.getInboxTable();
                Contact contId = new Contact((int) inboxTable.getValueAt(jt.getSelectedRow(), 3));
                message.setContId(contId);
                
              messageDao.save(message);
            } else {
                message.setContId(new Contact(Home.getContIdForUpdate()));
                messageDao.save(message);
            }   
            
            JOptionPane.showMessageDialog(rootPane, "Mensagem enviada com sucesso!", 
                "Sucesso", JOptionPane.INFORMATION_MESSAGE);
            this.setVisible(false);  
            databaseConnection.getConnection().close();  
        } catch (TreatmentException treatmentException) {
        JOptionPane.showMessageDialog(rootPane, treatmentException.getMessage(),
            "Campos inválidos", JOptionPane.ERROR_MESSAGE);
        } catch (SQLException ex) {
            switch (ex.getErrorCode()) {
                case 1406 -> JOptionPane.showMessageDialog(rootPane, 
                            "Tamanho máximo de 255 caracteres excedido",
                            "Erro", JOptionPane.ERROR_MESSAGE);
                default -> JOptionPane.showMessageDialog(rootPane,           
                        "Erro inesperado aconteceu! " + ex.getErrorCode() + ex.getMessage(),
                        "Erro", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_saveBtnActionPerformed

