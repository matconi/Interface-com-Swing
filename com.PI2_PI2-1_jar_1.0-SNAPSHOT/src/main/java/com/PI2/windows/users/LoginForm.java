    public User createCurrentUser(ResultSet rs) throws SQLException {
        User currentUser = new User(
            rs.getInt("id"), rs.getString("username"));
        return currentUser; 
    }
    
    public void validateUser() throws TreatmentException, SQLException {
        DatabaseConnection databaseConnection = new DatabaseConnection(
            "root", Env.getPass(), "manager_messages", "localhost", 3306);
      
        UserDao userDao = new UserDao(databaseConnection);
        ResultSet rs = userDao.login(userField.getText(), passwordField.getText());
        
        if (rs.next()) {
            Home home = new Home(createCurrentUser(rs));
            home.setVisible(true);           
            this.setVisible(false);
            
        } else {
            throw new TreatmentException("Usu치rio ou senha inv치lidos!");
        }  
        databaseConnection.getConnection().close();         
    }
